package ch07_array_1019;

public class TowDimension {

	public static void main(String[] args) {
		
		char[][] arr = {{'a','b'},{'c','d'},{'e','f'}};
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		for(int i=0; i<arr.length; i++){
			for(int j=0; j<arr[i].length; j++){
				System.out.print(arr[i][j]);
			}
			System.out.println();
		}
	}

}
